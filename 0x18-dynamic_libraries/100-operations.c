# include <stdio.h>

/*
 * add-sum of two integers
 * return sum
 */
int add(int a, int b)
{
	return (a + b);
}

/*
 * sub-subtraction of two integers
 * return subtract
 */ 
int sub(int a, int b)
{
	return (a - b);
}
/*
 * mul-multiplication of two integers
 * return multiplication
 */
int mul(int a, int b)
{
	return (a * b);
}

/*
 * div-divition of two integers
 * return divition
 */
int div(int a, int b)
{
	return (a / b);
}

/*
 * mod-modulus of two integers
 * return modulus
 */
int mod(int a, int b)
{
	return (a % b);
}
